package com.sanxia.oa.bean;
/**
 *作者：杨 赢
 *时间：2018年3月7日
 *作用: 在线请假实体类
 */
public class Leave {
	//主键ID
	private int leaveId;
	//用户ID
	private int userId;
	//部门ID
	private int deptId;
	//用户真实姓名
	private String realName;
	//请假开始时间
	private String startTime;
	//请假结束时间
	private String endTime;
	//请假原因
	private String leaveReason;
    //请假状态
	private int statement;
	
	public int getLeaveId() {
		return leaveId;
	}
	public void setLeaveId(int leaveId) {
		this.leaveId = leaveId;
	}
	public int getUserId() {
		return userId;
	}
	public void setUserId(int userId) {
		this.userId = userId;
	}
	
	public int getDeptId() {
		return deptId;
	}
	public void setDeptId(int deptId) {
		this.deptId = deptId;
	}
	public String getRealName() {
		return realName;
	}
	public void setRealName(String realName) {
		this.realName = realName;
	}
	public String getStartTime() {
		return startTime;
	}
	public void setStartTime(String startTime) {
		this.startTime = startTime;
	}
	public String getEndTime() {
		return endTime;
	}
	public void setEndTime(String endTime) {
		this.endTime = endTime;
	}
	public String getLeaveReason() {
		return leaveReason;
	}
	public void setLeaveReason(String leaveReason) {
		this.leaveReason = leaveReason;
	}
	public int getStatement() {
		return statement;
	}
	public void setStatement(int statement) {
		this.statement = statement;
	}
	public Leave(int userId, int deptId, String realName, String startTime,
			String endTime, String leaveReason) {
		super();
		this.userId = userId;
		this.deptId = deptId;
		this.realName = realName;
		this.startTime = startTime;
		this.endTime = endTime;
		this.leaveReason = leaveReason;
		
	}
	public Leave() {
		super();
	}
	@Override
	public String toString() {
		return "Leave [leaveId=" + leaveId + ", userId=" + userId + ", deptId="
				+ deptId + ", realName=" + realName + ", startTime="
				+ startTime + ", endTime=" + endTime + ", leaveReason="
				+ leaveReason + ", statement=" + statement + "]";
	}
	
}
