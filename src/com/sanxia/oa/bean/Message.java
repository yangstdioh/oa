package com.sanxia.oa.bean;
/**
 *作者：杨 赢
 *时间：2018年3月17日
 *作用: 信息实体类
 */
public class Message {
	//信息ID
	private int mesId;
	//发送者ID
	private int sendId;
	//发送用户名
	private String sendName;
	//发送时间
	private String time;
	//接收者ID
	private int receiveId;
	//信息内容
	private String message;
	//信息状态
	private int statement;
	
	public int getMesId() {
		return mesId;
	}
	public void setMesId(int mesId) {
		this.mesId = mesId;
	}
	public int getSendId() {
		return sendId;
	}
	public void setSendId(int sendId) {
		this.sendId = sendId;
	}
	public String getSendName() {
		return sendName;
	}
	public void setSendName(String sendName) {
		this.sendName = sendName;
	}
	public String getTime() {
		return time;
	}
	public void setTime(String time) {
		this.time = time;
	}
	public int getReceiveId() {
		return receiveId;
	}
	public void setReceiveId(int receiveId) {
		this.receiveId = receiveId;
	}
	public String getMessage() {
		return message;
	}
	public void setMessage(String message) {
		this.message = message;
	}
	public int getStatement() {
		return statement;
	}
	public void setStatement(int statement) {
		this.statement = statement;
	}
	
	public Message(int sendId, String sendName, String time, int receiveId,
			String message, int statement) {
		super();
		this.sendId = sendId;
		this.sendName = sendName;
		this.time = time;
		this.receiveId = receiveId;
		this.message = message;
		this.statement = statement;
	}
	public Message() {
		super();
	}
	@Override
	public String toString() {
		return "Message [mesId=" + mesId + ", sendId=" + sendId + ", sendName="
				+ sendName + ", time=" + time + ", receiveId=" + receiveId
				+ ", message=" + message + ", statement=" + statement + "]";
	}
	
}
